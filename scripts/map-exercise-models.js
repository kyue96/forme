#!/usr/bin/env node
/**
 * Auto-maps GLB model files in assets/models/ to exercise names.
 *
 * Usage:
 *   node scripts/map-exercise-models.js
 *
 * How it works:
 * 1. Scans assets/models/ for .glb files
 * 2. Converts filenames to keywords (e.g. "bicep-curl.glb" → "bicep curl")
 * 3. Finds all matching exercises from EXERCISE_INSTRUCTIONS in exercise-data.ts
 * 4. Generates lib/exercise-models.ts with all mappings
 *
 * Naming convention for GLB files:
 *   - Use lowercase kebab-case: bench-press.glb, lat-pulldown.glb
 *   - Name should match the core exercise: "squat.glb" will match
 *     "barbell squat", "back squat", "front squat", "goblet squat", etc.
 */

const fs = require('fs');
const path = require('path');

const MODELS_DIR = path.join(__dirname, '..', 'assets', 'models');
const EXERCISE_DATA = path.join(__dirname, '..', 'lib', 'exercise-data.ts');
const OUTPUT = path.join(__dirname, '..', 'lib', 'exercise-models.ts');

// 1. Read all .glb files
const glbFiles = fs.readdirSync(MODELS_DIR).filter(f => f.endsWith('.glb'));
if (glbFiles.length === 0) {
  console.log('No .glb files found in assets/models/');
  process.exit(0);
}
console.log(`Found ${glbFiles.length} model(s): ${glbFiles.join(', ')}`);

// 2. Extract exercise names from EXERCISE_INSTRUCTIONS in exercise-data.ts
const exerciseDataSrc = fs.readFileSync(EXERCISE_DATA, 'utf8');
const exerciseNames = [];
// Match keys like: 'barbell bench press': '...'
const keyRegex = /^\s*'([^']+)':\s*'/gm;
let match;
while ((match = keyRegex.exec(exerciseDataSrc)) !== null) {
  exerciseNames.push(match[1]);
}
console.log(`Found ${exerciseNames.length} exercises in exercise-data.ts`);

// 3. Build mappings: for each GLB, find exercises whose name contains the GLB keyword
const mappings = []; // { exerciseName, glbFile }

for (const glb of glbFiles) {
  const basename = glb.replace('.glb', '');
  // Convert kebab-case to space-separated keywords
  const keywords = basename.split('-').map(s => s.toLowerCase());
  const keywordStr = keywords.join(' ');

  // Find all exercises that contain the keyword
  const matched = exerciseNames.filter(name => {
    const lower = name.toLowerCase();
    // Exact keyword match or keyword is a substring
    if (lower.includes(keywordStr)) return true;
    // Also match if all individual keywords appear in the name
    if (keywords.length > 1 && keywords.every(kw => lower.includes(kw))) return true;
    return false;
  });

  // Always include the keyword itself as a direct mapping
  mappings.push({ exerciseName: keywordStr, glbFile: glb });

  for (const name of matched) {
    if (name !== keywordStr) {
      mappings.push({ exerciseName: name, glbFile: glb });
    }
  }

  console.log(`  ${glb} → ${matched.length + 1} mapping(s): "${keywordStr}"${matched.length > 0 ? ', ' + matched.map(m => `"${m}"`).join(', ') : ''}`);
}

// 4. Generate exercise-models.ts
const requiresByGlb = {};
for (const glb of glbFiles) {
  const varName = glb.replace('.glb', '').replace(/[^a-zA-Z0-9]/g, '_');
  requiresByGlb[glb] = varName;
}

let output = `/**
 * Auto-generated by scripts/map-exercise-models.js
 * Maps exercise names (lowercase) to GLB model assets.
 *
 * To regenerate: node scripts/map-exercise-models.js
 * To add models: drop .glb files in assets/models/ and rerun the script.
 */

`;

// Require statements
for (const [glb, varName] of Object.entries(requiresByGlb)) {
  output += `const ${varName} = require('../assets/models/${glb}');\n`;
}

output += `\nconst MODEL_MAP: Record<string, any> = {\n`;
for (const { exerciseName, glbFile } of mappings) {
  const varName = requiresByGlb[glbFile];
  output += `  '${exerciseName}': ${varName},\n`;
}
output += `};\n`;

output += `
/**
 * Returns the GLB model asset for a given exercise name, or null if no model exists.
 */
export function getExerciseModel(exerciseName: string): any | null {
  const key = exerciseName.toLowerCase().trim();
  // Exact match
  if (MODEL_MAP[key]) return MODEL_MAP[key];
  // Partial match — check if exercise name contains a mapped key
  for (const [mapKey, asset] of Object.entries(MODEL_MAP)) {
    if (key.includes(mapKey) || mapKey.includes(key)) return asset;
  }
  return null;
}

/**
 * Check if an exercise has a 3D model available.
 */
export function hasExerciseModel(exerciseName: string): boolean {
  return getExerciseModel(exerciseName) !== null;
}
`;

fs.writeFileSync(OUTPUT, output, 'utf8');
console.log(`\nGenerated ${OUTPUT} with ${mappings.length} mapping(s)`);
