#!/usr/bin/env node
/**
 * Auto-maps rendered exercise pose PNGs to exercise names.
 *
 * Usage:
 *   node scripts/map-exercise-poses.js
 *
 * Scans assets/exercise-poses/ for *-start.png + *-end.png pairs
 * and generates lib/exercise-poses.ts with all mappings.
 */

const fs = require('fs');
const path = require('path');

const POSES_DIR = path.join(__dirname, '..', 'assets', 'exercise-poses');
const EXERCISE_DATA = path.join(__dirname, '..', 'lib', 'exercise-data.ts');
const OUTPUT = path.join(__dirname, '..', 'lib', 'exercise-poses.ts');

// 1. Find all pose pairs
if (!fs.existsSync(POSES_DIR)) {
  console.log('No assets/exercise-poses/ directory found. Run the Blender script first.');
  process.exit(0);
}

const files = fs.readdirSync(POSES_DIR);
const startFiles = files.filter(f => f.endsWith('-start.png'));
const exercises = startFiles.map(f => f.replace('-start.png', '')).filter(name => {
  return files.includes(`${name}-end.png`);
});

if (exercises.length === 0) {
  console.log('No pose pairs found (need *-start.png + *-end.png)');
  process.exit(0);
}
console.log(`Found ${exercises.length} exercise pose pair(s): ${exercises.join(', ')}`);

// 2. Extract exercise names from exercise-data.ts
const exerciseDataSrc = fs.readFileSync(EXERCISE_DATA, 'utf8');
const exerciseNames = [];
const keyRegex = /^\s*'([^']+)':\s*'/gm;
let match;
while ((match = keyRegex.exec(exerciseDataSrc)) !== null) {
  exerciseNames.push(match[1]);
}

// 3. Build mappings
const mappings = []; // { exerciseName, poseKey }

for (const pose of exercises) {
  const keywords = pose.split('-').map(s => s.toLowerCase());
  const keywordStr = keywords.join(' ');

  const matched = exerciseNames.filter(name => {
    const lower = name.toLowerCase();
    if (lower.includes(keywordStr)) return true;
    if (keywords.length > 1 && keywords.every(kw => lower.includes(kw))) return true;
    return false;
  });

  mappings.push({ exerciseName: keywordStr, poseKey: pose });
  for (const name of matched) {
    if (name !== keywordStr) {
      mappings.push({ exerciseName: name, poseKey: pose });
    }
  }

  console.log(`  ${pose} → ${matched.length + 1} mapping(s)`);
}

// 4. Generate lib/exercise-poses.ts
let output = `/**
 * Auto-generated by scripts/map-exercise-poses.js
 * Maps exercise names to start/end pose image assets.
 *
 * To regenerate: node scripts/map-exercise-poses.js
 */

export interface ExercisePose {
  start: any;
  end: any;
}

`;

// Require statements for each pose pair
for (const pose of exercises) {
  const varName = pose.replace(/[^a-zA-Z0-9]/g, '_');
  output += `const ${varName} = {\n`;
  output += `  start: require('../assets/exercise-poses/${pose}-start.png'),\n`;
  output += `  end: require('../assets/exercise-poses/${pose}-end.png'),\n`;
  output += `};\n\n`;
}

output += `const POSE_MAP: Record<string, ExercisePose> = {\n`;
for (const { exerciseName, poseKey } of mappings) {
  const varName = poseKey.replace(/[^a-zA-Z0-9]/g, '_');
  output += `  '${exerciseName}': ${varName},\n`;
}
output += `};\n\n`;

output += `/**
 * Returns start/end pose images for an exercise, or null if not available.
 */
export function getExercisePoses(exerciseName: string): ExercisePose | null {
  const key = exerciseName.toLowerCase().trim();
  if (POSE_MAP[key]) return POSE_MAP[key];
  // Partial match
  for (const [mapKey, poses] of Object.entries(POSE_MAP)) {
    if (key.includes(mapKey) || mapKey.includes(key)) return poses;
  }
  return null;
}
`;

fs.writeFileSync(OUTPUT, output, 'utf8');
console.log(`\\nGenerated ${OUTPUT} with ${mappings.length} mapping(s)`);
